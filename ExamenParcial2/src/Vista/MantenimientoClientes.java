    public void llenadoDeTablas() {
DefaultTableModel modelo = new DefaultTableModel();
        modelo.addColumn("ID Cliente");
        modelo.addColumn("Nombre");
        modelo.addColumn("Apellido");
        modelo.addColumn("Nit");
        modelo.addColumn("Telefono");
        modelo.addColumn("Direccion");
        modelo.addColumn("Correo");
        modelo.addColumn("Estatus");
        ClienteDAO clienteDAO = new ClienteDAO();
        List<Cliente> Cliente = clienteDAO.select();
        tablaVendedores.setModel(modelo);
        String[] dato = new String[8];
        for (int i = 0; i < Cliente.size(); i++) {
            dato[0] = Cliente.get(i).getPkid();
            dato[1] = Cliente.get(i).getNombre();
            dato[2] = Cliente.get(i).getApellido();
            dato[3] = Cliente.get(i).getNit();
            dato[4] = Cliente.get(i).getTelefono();
            dato[5] = Cliente.get(i).getDireccion();
            dato[6] = Cliente.get(i).getCorreo();
            dato[7] = Cliente.get(i).getEstatus();
            modelo.addRow(dato);
        }
    }

    public void buscarVendedor() {
    DefaultTableModel modelo = new DefaultTableModel();
    modelo.addColumn("ID Cliente");
    modelo.addColumn("Nombre");
    modelo.addColumn("Apellido");
    modelo.addColumn("Nit");
    modelo.addColumn("Telefono");
    modelo.addColumn("Direccion");
    modelo.addColumn("Correo");
    modelo.addColumn("Estatus");
    ClienteDAO clienteDAO = new ClienteDAO();
    List<Cliente> clientes = clienteDAO.select();
    tablaVendedores.setModel(modelo);
    for (Cliente cliente : clientes) {
        Object[] fila = new Object[]{
            cliente.getPkid(),
            cliente.getNombre(),
            cliente.getApellido(),
            cliente.getNit(),
            cliente.getTelefono(),
            cliente.getDireccion(),
            cliente.getCorreo(),
            cliente.getEstatus()
        };
        modelo.addRow(fila);
    }
    }

